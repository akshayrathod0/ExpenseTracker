    private void getEntries() {
        try {
            javax.swing.table.DefaultTableModel tdm
                        = (javax.swing.table.DefaultTableModel)table.getModel();
            int rc = tdm.getRowCount();  // this is for empty the table
                while(rc--!=0){
                    tdm.removeRow(0);
                }
            ResultSet rs = Db.Db_Connection.st.executeQuery("select * from category_info");
            int srno = 0;
            while (rs.next()) {  // this is for fetching data from the category table
                String category = rs.getString("category");
       
                
//                Object o[]={++srno,category};
//                tdm.addRow(o);
                java.util.Vector row = new java.util.Vector<>();
                row.add(++srno);
                row.add(category);
                tdm.addRow(row);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

