    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed

        try {
            String category = textfield1.getText();
            
            if(!category.equals("")){
            Db.Db_Connection.st.executeUpdate("insert into category_info values('" + category + "')");
            JOptionPane.showMessageDialog(null, "Category Addedd Successfully");
            getEntries();
            }else{
              JOptionPane.showMessageDialog(null, "Please Enter the Category");

            }
        } catch (SQLIntegrityConstraintViolationException ex) {
            JOptionPane.showMessageDialog(null, "Category Already Existed");
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
          int ri = table.getSelectedRow();
        if(ri!=-1){
        int r = JOptionPane.showConfirmDialog(null,"Do you wanna delete really",
                  "Deletion Conformation",JOptionPane.YES_NO_OPTION);
          if(r==JOptionPane.YES_OPTION){
            String category = (String)table.getValueAt(ri,1);
            try{       
            Db.Db_Connection.st.executeUpdate("delete from category_info where category='"+category+"'");
            JOptionPane.showMessageDialog(null,
                    "Category Deleted Successfully");
            getEntries();
            } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
          }
        }
     
    }//GEN-LAST:event_jButton2ActionPerformed

