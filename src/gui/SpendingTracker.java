    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int ri = table1.getSelectedRow();
        if (ri != -1) {
            int id = (int) table1.getValueAt(ri, 0);
            int r = JOptionPane.showConfirmDialog(null, "Do you really wanna delete",
                    "Delete Comformation", JOptionPane.YES_NO_OPTION);
            try {
                Db.Db_Connection.st.executeUpdate("delete from expense where e_id=" + id);
                JOptionPane.showMessageDialog(null,
                        "Data Successfully Deleted");
                getEntries();
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(null, ex);
            }
        }

    }//GEN-LAST:event_jButton3ActionPerformed

