    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            java.util.Date dt = datechooser.getDate();
            String s1 = amount.getText();
            String c = (String) category.getSelectedItem();
            if (dt != null && !s1.equals("") && !c.equals("")) {
                int amount = Integer.parseInt(s1);
                java.sql.Date date = new java.sql.Date(dt.getTime());
                Db.Db_Connection.st.executeUpdate("insert into expense (category,date,amount) "
                        + "values('" + c + "','" + date + "'," + amount + ")");
                JOptionPane.showMessageDialog(null,
                        "Expense Added Successfully");
                getEntries();
            } else {
                JOptionPane.showMessageDialog(null,
                        "Please fill all the field");
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

