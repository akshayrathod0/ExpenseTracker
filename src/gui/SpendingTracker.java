    private void getEntries() {
        try {
            javax.swing.table.DefaultTableModel dtm
                    = (javax.swing.table.DefaultTableModel) table1.getModel();
            int rc = dtm.getRowCount();
            while (rc-- != 0) {
                dtm.removeRow(rc);
            }
            java.time.LocalDate cd = java.time.LocalDate.now();
            java.time.LocalDate bd = cd.minusDays(20);
            ResultSet rs = Db.Db_Connection.st.executeQuery(
                    "select * from expense where date<='" + cd + "' and date>='" + bd + "'");
            int total = 0;

            while (rs.next()) {
                total += rs.getInt("amount");
                Object o[] = {rs.getInt("e_id"),
                    rs.getString("category"), rs.getDate("date"), total};
                dtm.addRow(o);
            }
            TotalAmount.setText(total + "");

        } catch (Exception ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }

    private void displayCategory() {
        try {
            category.removeAllItems();
            ResultSet rs = Db.Db_Connection.st.executeQuery("select * from category_info");
            while (rs.next()) {
                category.addItem(rs.getString("category"));
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }
    }

